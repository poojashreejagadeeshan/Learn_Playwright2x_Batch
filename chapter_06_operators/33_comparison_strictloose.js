// loose comparison compares values after coersion 
console.log(42 == "42");
console.log(42 == 42);

console.log("-------------")

console.log(0 == "");
console.log(0 == 0);

console.log("-------------")

console.log(true == 1);
console.log(false == 0);

console.log("-------------")

// strict comparison compares values without coersion
console.log(5 === 5);
console.log(5 === "5");

console.log(0 === "")
console.log(0 === 0);
console.log(true === 1);
console.log(true === 2);

